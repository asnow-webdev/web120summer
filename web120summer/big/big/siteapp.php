<h2 class="pageID">Responsive Website V.S. Native Mobile App Research</h2>
     

     <p>So, you’re wondering if you making your website mobile responsive is the way to go or if you should invest in a native app.  Before making this decision, there are a few questions you need to answer.</p>

     <h4>Why is my audience using my site/app?</h4>

     <p>Okay, so the first question isn’t “why do I need a responsive site or native app?”.  That should go without saying.  You need to have your presence on the web mobile-ready or you are shooting yourself in the foot no matter what your web purpose.  As of 2014, mobile users of the web surpassed desktop users.</p>

     <p>You need to know why your audience is coming to use your site.  Is it for general information?  Are they needing to learn what your offerings are?  Will they be coming to your site infrequently as there is little interaction needed?  If so, then a responsive website should fit the bill.  Maybe they just need your physical address or contact info.  This is the kind of information that would be well suited to be delivered by a responsive website.</p>

     <p>On the other hand, will they be coming back for multiple interactions?  Do you provide a service or product that they will need repeatedly?  Do they have a specific goal they are trying to achieve in as little time as possible and, most likely, numerous times?  If so, then a native app will be the best functional option.</p>  

     <h4>How much do I have to invest?</h4>

     <p>I’m talking resources here.  Not just dollars, but staff and hours as well.  Updating your website to be responsive does not require a huge investment in any of these.  It can be attained by adding, or modifying, CSS3 files to style your current website and content so that it will adapt to any device that it is displayed on.  A huge benefit of a responsive site is that it is basically platform-neutral.  You do the work once and it is applicable on most, if not all, browsers.</p>

     <p>Creating a native app is another story.  Apps are designed to work on mobile device platforms, primarily Android and iOS, so they are platform-specific.  Meaning a great deal of the work you do on one platform is not transferable to the other platform.</p>

     <h4>Condensed down to points:</h4>

     <ul>Responsive sites:
        <li>Faster to build and launch and lower cost</li>
        <li>Reach more potential users</li>
        <li>Easier to update and debug</li>
        <li>Not as fast or functional as a native app</li>
    </ul>
     <ul>Native apps:
        <li>Greater functionality and speed to complete the goal</li>
        <li>Increased interactivity with user through repeated use and push notifications</li>
        <li>Branding, branding, branding</li>
        <li>More labor-intensive to build, deploy, and maintain</li>
        <li>More cost to create from scratch</li>
     </ul>

 
