<h2 class="pageID">Shopping Cart Research</h2>
 <p>When assessing how to incorporate a shopping cart into your website there are two key aspects that must be addressed; user experience and shopping cart abandonment.  Luckily, they are interconnected so addressing one also has the benefit of addressing the other.</p>
<p>A widely accepted approach, and proven successful across numerous industries, is the KISS (Keep It Simple, Silly) approach.  Using this approach helps us to break down how we address the design into two distinct areas:  aesthetic design and functional design/process.</p>
<h3>Aesthetic Design</h3>
<p>First and foremost, the look and feel of the shopping cart page should continue on the theme from the main site.  “When there’s a visual disconnect between the checkout experience and the rest of your site, it can contribute to abandonment at the first step … by maintaining exact styles and keeping an identical look and feel, your site can prevent visitors from feeling visually alarmed. So, they will continue on as if everything is a part of one experience.”<sup>1</sup></p>
<p>Second, try to keep the number of pages to a minimum.  In fact, encapsulating the entire checkout process on one page is optimal and can be achieved through thoughtful functional design (see, they are related).</p>
<p>Third, if at all possible you should strive to have the contents of the shopping cart on every page of the checkout process so the client never loses touch with their purchase.  Items to include would be:</p>     <ul>
        <li>An image of the product</li>
        <li>Description of the product</li>
        <li>Quantity and Price of each item</li>
        <li>The subtotal price, before shipping and discounts applied</li>
     </ul>
     
<h3>Functional/Process Design</h3>
<p>Staying with the KISS approach should inform your process design to further enhance the usability and stickiness of your shopping cart checkout page. </p> 
<p><b>Display</b> – Use a table format to display the different sections of the checkout process on one page.  This can be achieved either in a column-format or row-format.  With the row-format, it is possible to use collapsible rows to keep process visibility while maintaining white space.</p>
<p><b>Information gathering process</b> – In a general format, most shopping cart checkouts require the following information; Shipping Address, Billing Address, Shipping Method, and Payment Method.  It is recommended that the process should be in this order as well.  Also, while companies would like to associate any sale with a registered user, the checkout process should not be held up by insisting on log in or account creation if at all possible.  If they choose to checkout as a guest, then require their email in the shipping info which can be utilized for reporting and marketing later.</p>
<p>These are a few key areas to address when setting up your shopping cart and checkout process.  The depth to which you go in these areas is your choice.
</p>
 
